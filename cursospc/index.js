function direcao(e) {


    var direcao = document.getElementById("menu-produtos");
    if (e == 1 ) {
        //esquerda
        direcao.scrollLeft = direcao.scrollLeft - 300 ;

    }else if(e == 2){
        //direita
        direcao.scrollLeft = direcao.scrollLeft + 300;
    }
    
}
